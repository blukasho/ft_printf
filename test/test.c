void			print_double_bits(long double d)
{               
	int			i;
	char		b[81];
	__int128	li;

	li = *(__int128 *)&d;
	i = 0;
	printf("\n");
	while (i <= 80)
	{
		if ((li & 0x1) == 1)
			b[i] = '1';
		else
			b[i] = '0';
		++i;
		li = li >> 1;
	}
	b[i] = '\0';
	i = 79;
	while (i >= 0)
		printf("%c", b[i--]);
	printf("\n");
}

